package com.diycomputerscience.resourcelist.view;

public class ViewException extends java.lang.Exception {

	public ViewException() {
		super();
	}
	
	public ViewException(String msg) {
		super(msg);
	}
	
	public ViewException(Throwable cause) {
		super(cause);
	}
	
	public ViewException(String msg, Throwable cause) {
		super(msg, cause);
	}
}
